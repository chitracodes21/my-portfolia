// ============================================================
//  SPARK EFFECT
// ============================================================
(function () {
  const canvas = document.getElementById("sparkCanvas");
  if (!canvas) return;
  const ctx = canvas.getContext("2d");
  let disableSparks = false;
  window.disableSparks = disableSparks;

  function resizeCanvas() {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
  }
  window.addEventListener("resize", resizeCanvas);
  resizeCanvas();

  let sparks = [];
  let animationId = null;
  const SPARK_COLOR = "#ffd700",
    SPARK_RADIUS = 35,
    SPARK_COUNT = 10,
    DURATION = 500;
  const easeOutCubic = (t) => 1 - Math.pow(1 - t, 3);

  function addSpark(e) {
    if (window.disableSparks) return;
    const x = e.clientX,
      y = e.clientY,
      now = performance.now();
    for (let i = 0; i < SPARK_COUNT; i++) {
      const angle = (2 * Math.PI * i) / SPARK_COUNT;
      sparks.push({ x, y, angle, startTime: now });
    }
  }

  function draw(timestamp) {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    sparks = sparks.filter((s) => timestamp - s.startTime < DURATION);
    for (const s of sparks) {
      const elapsed = timestamp - s.startTime;
      const progress = Math.min(1, elapsed / DURATION);
      const eased = easeOutCubic(progress);
      const distance = eased * SPARK_RADIUS;
      const lineLength = 12 * (1 - eased);
      const x1 = s.x + distance * Math.cos(s.angle),
        y1 = s.y + distance * Math.sin(s.angle);
      const x2 = s.x + (distance + lineLength) * Math.cos(s.angle),
        y2 = s.y + (distance + lineLength) * Math.sin(s.angle);
      ctx.beginPath();
      ctx.moveTo(x1, y1);
      ctx.lineTo(x2, y2);
      ctx.strokeStyle = SPARK_COLOR;
      ctx.lineWidth = 2.5;
      ctx.stroke();
    }
    if (sparks.length) animationId = requestAnimationFrame(draw);
    else {
      cancelAnimationFrame(animationId);
      animationId = null;
    }
  }

  function startAnimationIfNeeded() {
    if (!animationId && sparks.length)
      animationId = requestAnimationFrame(draw);
  }

  document.addEventListener("click", function (e) {
    addSpark(e);
    startAnimationIfNeeded();
  });
})();

