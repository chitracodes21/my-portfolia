function renderLearningList() {
  const container = document.getElementById("learning-posts-list");
  if (!container) return;
  container.innerHTML = "";
  blogPosts.forEach((post) => {
    const link = document.createElement("a");
    link.href = `#post/${post.slug}`;
    link.className =
      "learning-post flex items-center justify-between w-full group py-0.5";
    link.setAttribute("data-slug", post.slug);
    link.innerHTML = `
      <p class="font-semibold text-sm group-hover:underline underline-offset-4 text-olive-800 dark:text-olive-100">${post.title}</p>
      <p class="text-sm text-olive-500 dark:text-olive-400 font-mono uppercase">${post.date}</p>
    `;
    link.addEventListener("click", (e) => {
      e.preventDefault();
      window.location.hash = `post/${post.slug}`;
      handleRoute();
    });
    container.appendChild(link);
  });
}

