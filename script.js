function setPageTitle(title) {
  document.title = title;
}

function getHashPage() {
  const hash = window.location.hash.slice(1) || "home";
  const parts = hash.split("/");
  return parts[0] || "home";
}

function getHashSlug() {
  const hash = window.location.hash.slice(1);
  const parts = hash.split("/");
  return parts[1] || null;
}

function handleRoute() {
  const page = getHashPage();
  const slug = getHashSlug();

  // Close blog view if open
  if (blogView.classList.contains("active-page")) {
    blogView.classList.remove("active-page", "no-animation");
    header.classList.remove("hidden-header");
    if (window.disableSparks) window.disableSparks = false;
  }

  if (page === "post" && slug) {
    openBlogPost(slug);
    return;
  }

  if (["home", "learning", "notes", "coding"].includes(page)) {
    showPage(page);
  } else {
    showPage("home");
  }
}

function showPage(pageId) {
  Object.values(pages).forEach(
    (p) => p && p.classList.remove("active-page", "no-animation"),
  );
  if (pages[pageId]) {
    pages[pageId].classList.add("active-page");
    pages[pageId].classList.remove("no-animation");
  }

  navLinks.forEach((link) => {
    const lp = link.getAttribute("data-page");
    if (lp === pageId) {
      link.classList.add("active");
      link.classList.remove("text-olive-500", "dark:text-olive-400");
    } else {
      link.classList.remove("active");
      link.classList.add("text-olive-500", "dark:text-olive-400");
    }
  });

  if (pageTitles[pageId]) {
    setPageTitle(pageTitles[pageId]);
  } else {
    setPageTitle("Chitra Shrestha @accountant");
  }

  window.scrollTo(0, 0);
  // Re-animate ALL blur-in elements (header, footer, body) on every page change
  setTimeout(resetAndObserveElements, 150);
}

