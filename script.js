//  BLUR-IN OBSERVER – resets and re-observes ALL elements
//  (header, footer, body) on every navigation
// ============================================================
let currentObserver = null;

function resetAndObserveElements() {
  // Kill previous observer if exists
  if (currentObserver) {
    currentObserver.disconnect();
    currentObserver = null;
  }

  // Reset all blur-in elements (remove 'show' class so they can animate again)
  document.querySelectorAll(".blur-in, .stagger > *").forEach((el) => {
    el.classList.remove("show");
  });

  // Create new observer
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("show");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.15 },
  );

  // Observe all blur-in elements, but skip blog content
  document.querySelectorAll(".blur-in, .stagger > *").forEach((el) => {
    if (el.closest("#blog-post-view")) return;
    observer.observe(el);
  });

  currentObserver = observer;
}

